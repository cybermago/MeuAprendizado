package loop;

public class FOR {
	public static void main(String[] args) {
	
		int[] array={10,20,30,40};
		
		for(int i=0; i<=array.length; i++) {                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                              
			int linha = array[i];
			System.out.println(linha);
		}
	}
}